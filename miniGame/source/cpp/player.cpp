#include "player.h"

//=============================================================================
// マクロ定義
//=============================================================================

//=============================================================================
// 静的メンバ変数宣言
//=============================================================================
int CPlayer::m_nObjectPlayerNum = 0;
int CPlayer::m_nRank = 0;

//=============================================================================
// デフォルトコンストラクタ
//=============================================================================
CPlayer::CPlayer()
{
	//総数を加算
	m_nObjectPlayerNum++;
	//決まっているランキングを増やす
	m_nRank++;
	m_nIndex = m_nObjectPlayerNum;
	m_nRanking = 0;
	m_bUpdate = false;

}
//=============================================================================
// デストラクタ
//=============================================================================
CPlayer::~CPlayer()
{

}

//=============================================================================
// 生成処理
//=============================================================================
CPlayer* CPlayer::Create() {
	CPlayer* pPlayer;
	pPlayer = new CPlayer();
	if (pPlayer == nullptr) return nullptr;

	pPlayer->Init();

	return pPlayer;
}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CPlayer::Init(void) {
	
	//変数初期化
	m_bUpdate = true;
	m_nRanking = 0;

	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void CPlayer::Uninit(void) {

	//総数を減らす
	m_nObjectPlayerNum--;
	//決まっているランキングをリセット
	m_nRank = 0;

	//オブジェクトの破棄
	Release();
}

//=============================================================================
// 更新処理
//=============================================================================
void CPlayer::Update(void) {
	
}

//=============================================================================
// ランキング設定処理
//=============================================================================
void CPlayer::SetRanking()
{
	m_nRanking = m_nRank;
	m_nRank--;
}
